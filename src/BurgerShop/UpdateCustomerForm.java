    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String id = txtOrderId.getText();
            Burger customerDetails = CustomerController.searchCustomer(id);
            if (customerDetails.getStatus().equals("PREPARING")) {
                String phone = txtCustomerId.getText();
                String Name = txtName.getText();
                int burgerQty = Integer.parseInt(txtOrderQty.getText());
                String status = (String) cmbName.getSelectedItem();
                if (burgerQty > 0) {
                    Burger customer = new Burger(id, phone, Name, burgerQty, status);
                    boolean isUpdate = CustomerController.updateCustomer(customer);
                    if (isUpdate) {
                        JOptionPane.showMessageDialog(this, "Updated");

                    } else {
                        JOptionPane.showMessageDialog(this, "Update Failed");

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Update Failed");

                }
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

