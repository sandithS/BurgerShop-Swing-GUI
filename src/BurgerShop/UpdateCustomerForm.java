    private void txtOrderIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtOrderIdActionPerformed
        try {
            String id = txtOrderId.getText();
            Burger customer = CustomerController.searchCustomer(id);
            if (customer == null) {
                JOptionPane.showMessageDialog(this, "Customer not found..");
            } else {
                cmbName.setSelectedItem(customer.getStatus());
                txtCustomerId.setText(customer.getCustomerId());
                txtOrderQty.setText(String.valueOf(customer.getBurgerQty()));
                txtName.setText(customer.getCustomerName() + "");
                if (cmbName.getSelectedItem().equals("PREPARING")) {
                    lblDisplay.setText("");
                    txtOrderQty.setEditable(true);
                    cmbName.setEnabled(true);
                } else {
                    txtOrderQty.setEditable(false);
                    cmbName.setEnabled(false);
                }
                if (cmbName.getSelectedItem().equals("DELIVERED")) {
                    lblDisplay.setText("<html>This order has been Delivered<br>Sorry. You can not update this order</html>");
                } else if (cmbName.getSelectedItem().equals("CANCELLED")) {
                    lblDisplay.setText("<html>This order has been Cancelled<br>Sorry. You can not update this order</html>");
                }
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_txtOrderIdActionPerformed

